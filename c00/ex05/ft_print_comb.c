#include <unistd.h>

void	ft_print_comb(void)
{
	char	str[5];

	str[0] = '0';
	str[3] = ',';
	str[4] = ' ';
	while (str[0] <= '6')
	{
		str[1] = str[0] + 1;
		while (str[1] <= '8')
		{
			str[2] = str[1] + 1;
			while (str[2] <= '9')
			{
				write(1, str, 5);
				str[2] += 1;
			}
			str[1] += 1;
		}
		str[0] += 1;
	}
	write(1, "789", 3);
}
