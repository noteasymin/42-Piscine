char	*ft_strlowcase(char *str)
{
	char	*tmp;

	tmp = str;
	while (*tmp != 0)
	{
		if (*tmp > 64 && *tmp < 91)
			*tmp += 32;
		tmp++;
	}
	return (str);
}
