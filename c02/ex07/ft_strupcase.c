char	*ft_strupcase(char *str)
{
	char	*tmp;

	tmp = str;
	while (*tmp != 0)
	{
		if (*tmp > 96 && *tmp < 123)
			*tmp -= 32;
		tmp++;
	}
	return (str);
}
