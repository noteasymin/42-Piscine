int	ft_str_is_numeric(char *str)
{
	int	flag;

	flag = 1;
	while (*str != 0)
	{
		if (!(*str > 47 && *str < 58))
		{
			flag = 0;
			break ;
		}
		str++;
	}
	return (flag);
}
